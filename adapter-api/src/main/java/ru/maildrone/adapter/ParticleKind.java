package ru.maildrone.adapter;

/**
 * Абстрактные виды частиц, используемые дроном и доставкой. Маппятся на
 * конкретные {@code org.bukkit.Particle} внутри версионного адаптера.
 */
public enum ParticleKind {
    /** Выхлоп винтов дрона в полёте (дым). */
    EXHAUST,
    /** Вылет с отделения (облачко). */
    DEPART,
    /** След дрона в полёте (искра/энд-род). */
    TRAIL,
    /** Прибытие посылки (частицы радости). */
    ARRIVE,
    /** Застряла на сортировке (раздражённые частицы). */
    STUCK
}
